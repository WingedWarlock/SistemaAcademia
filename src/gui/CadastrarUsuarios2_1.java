   public void mostrarDados( Usuarios usuarios ) {
        tfCpf.setText( String.valueOf( usuarios.getCpf() ) );
        tfNome.setText( String.valueOf( usuarios.getNome() ) );
        tfTelefone.setText( String.valueOf( usuarios.getTelefone() ) );
        tfData.setText( String.valueOf( usuarios.getData() ) );
        tfHora.setText( String.valueOf( usuarios.getHora() ) );
        tfLogin.setText( "" );
        tfSenha.setText( "" );
    }
    public void primeiro() {
        try {
                Usuarios usuarios = usuariosDao.primeiro();

                if ( usuarios != null) {
                    mostrarDados( usuarios );
                }                    
            
        } catch ( SQLException e ) {
            JOptionPane.showMessageDialog( this, "Erro: " + e.getMessage() );
        }
    }
  
