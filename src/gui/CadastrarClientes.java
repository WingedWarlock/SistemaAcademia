    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
            int cpf = Integer.parseInt( tfCpf.getText() );  
            String nome = tfNome.getText(); 
            String telefone = tfTelefone.getText();
            String data = tfData.getText();
            String hora = tfHora.getText();
            float peso = Float.parseFloat(tfPeso.getText());
            float gordura = Float.parseFloat(tfGordura.getText());
            float massa = Float.parseFloat(tfMassa.getText());
            Clientes clientes = new Clientes();
            clientes.setCpf( cpf );
            clientes.setNome( nome );
            clientes.setTelefone( telefone );
            clientes.setData( data );
            clientes.setHora( hora );
            clientes.setPeso( peso );
            clientes.setGordura( gordura );
            clientes.setMassa( massa );
            clientesDao.salvarRegistro( clientes );
            JOptionPane.showMessageDialog( this, "Sucesso" );
                        
            } catch ( SQLException e1 ) {
               JOptionPane.showMessageDialog( this, "Erro: " + e1.getMessage() ); 
            }       
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        String cpf = tfCpf.getText();  
         
        try {
            clientesDao.excluirRegistro( Integer.parseInt( cpf ) );
            JOptionPane.showMessageDialog( this, "Sucesso" );
        } catch ( SQLException e) {
            JOptionPane.showMessageDialog( this, "Erro: " + e.getMessage() );
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
         try {
                int cpf = Integer.parseInt( JOptionPane.showInputDialog( this, "Digite o cpf a pesquisar") );

                Clientes clientes = clientesDao.pesquisarClientes( cpf );

                if ( clientes != null ) {
                    mostrarDados( clientes );                    
                    JOptionPane.showMessageDialog( this, "Registro Encontrado." );
                } else {
                    JOptionPane.showMessageDialog( this, "Registro NÃ£o Encontrado ");
                }
        } catch ( SQLException e ) {
            JOptionPane.showMessageDialog( this, "Erro: " + e.getMessage() );
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

