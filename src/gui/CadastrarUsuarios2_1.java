    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
            int cpf = Integer.parseInt( tfCpf.getText() );  
            String nome = tfNome.getText(); 
            String telefone = tfTelefone.getText();
            String data = tfData.getText();
            String hora = tfHora.getText();
            String login = tfLogin.getText();
            String senha = tfSenha.getText();
            Usuarios usuarios = new Usuarios();
            usuarios.setCpf( cpf );
            usuarios.setNome( nome );
            usuarios.setTelefone( telefone );
            usuarios.setData( data );
            usuarios.setHora( hora );
            usuarios.setLogin( login );
            usuarios.setSenha( senha );
            usuariosDao.salvarRegistro( usuarios );
            JOptionPane.showMessageDialog( this, "Sucesso" );
                        
            } catch ( SQLException e1 ) {
               JOptionPane.showMessageDialog( this, "Erro: " + e1.getMessage() ); 
            }       
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        String cpf = tfCpf.getText();  
         
        try {
            usuariosDao.excluirRegistro( Integer.parseInt( cpf ) );
            JOptionPane.showMessageDialog( this, "Sucesso" );
        } catch ( SQLException e) {
            JOptionPane.showMessageDialog( this, "Erro: " + e.getMessage() );
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
         try {
                int cpf = Integer.parseInt( JOptionPane.showInputDialog( this, "Digite o cpf a pesquisar") );

                Usuarios usuarios = usuariosDao.pesquisarUsuarios( cpf );

                if ( usuarios != null ) {
                    mostrarDados( usuarios );                    
                    JOptionPane.showMessageDialog( this, "Registro Encontrado." );
                    tfLogin.setText( "" );
                    tfSenha.setText( "" );
                } else {
                    JOptionPane.showMessageDialog( this, "Registro Não Encontrado ");
                }
        } catch ( SQLException e ) {
            JOptionPane.showMessageDialog( this, "Erro: " + e.getMessage() );
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

